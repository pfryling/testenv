function job_post_init() {
    $args = array(
      'label' => 'Job Posting',
        'public' => true,
        'taxonomies' => array( 'post_tag' ),
        'show_ui' => true,
        'capability_type' => 'post',
        'hierarchical' => false,
        'rewrite' => array('slug' => 'job-post'),
        'query_var' => true,
        'supports' => array(
            'title',
            'editor',
            'excerpt',
            'trackbacks',
            'custom-fields',
            'comments',
            'revisions',
            'categories',
            'tags',
            'thumbnail',
            'author',
            'page-attributes',)
        );
    register_post_type( 'job-post', $args );
}
